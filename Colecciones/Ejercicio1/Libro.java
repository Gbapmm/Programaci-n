class Libro {
    String signatura;
    String titulo;
    String editorial;
    String autor;
    String isbn;
    int paginas;

    public Libro(String signatura, String titulo, String editorial, String autor, String isbn, int paginas) {
        this.signatura = signatura;
        this.titulo = titulo;
        this.editorial = editorial;
        this.autor = autor;
        this.isbn = isbn;
        this.paginas = paginas;
    }
}